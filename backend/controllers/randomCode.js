const crypto = require('crypto')
// ----------------------------------------------------------------------------
// ----------------------------------------------------------------------------
// ----------------------------------------------------------------------------
// ----------------------------------------------------------------------------
// ----------------------------------------------------------------------------
const randomCode = (min, max) =>{
  return new Promise((resolve, reject) => { 
    crypto.randomInt(min, max, (err, n) => {
      if( err ){
        reject( err );
      } else {
        resolve( n );
      }
    });
  });
}

module.exports = randomCode